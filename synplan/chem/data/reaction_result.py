"""Shared result types for reaction processing pipelines.

Used by standardization, filtering, and other batch-processing functions
that need a common result format.
"""

import json
from dataclasses import asdict, dataclass, field
from pathlib import Path

from chython.containers import ReactionContainer  # kept for ProcessResult.reactions


@dataclass
class ErrorEntry:
    """A single processing error."""

    original: str
    stage: str
    error_type: str
    message: str
    source_info: str = ""
    line_number: int | None = None


@dataclass
class FilteredEntry:
    """A reaction intentionally excluded (not an error)."""

    original: str
    reason: str
    source_info: str = ""
    line_number: int | None = None


@dataclass
class ProcessResult:
    """Legacy result of processing a batch of reactions.

    New high-throughput pipelines should return :class:`BatchResult` instead:
    workers pre-serialize output records and the parent only writes strings.
    This class is kept for older call sites/tests that still need to carry
    ReactionContainer objects.

    ``dedup_keys`` and ``smiles_strings`` are retained for backward
    compatibility with the earlier standardization patch.  Dedup keys must be
    stable CGR strings (``str(~rxn)``), not ``hash(~rxn)``. Python's built-in
    ``hash()`` is randomized per process (PYTHONHASHSEED), so the same reaction
    can produce different integers in different workers, breaking cross-worker
    deduplication.
    """

    reactions: list[ReactionContainer]
    filtered: list[FilteredEntry] = field(default_factory=list)
    errors: list[ErrorEntry] = field(default_factory=list)
    dedup_keys: list[str | None] = field(default_factory=list)
    smiles_strings: list[str] = field(default_factory=list)


@dataclass
class BatchResult:
    """Output of one worker batch.  Records are already serialized to the target format.

    Workers produce strings; the parent does only dedup lookup + file write.
    No ReactionContainer crosses the IPC boundary.
    """

    records: list[str]
    dedup_keys: list[str | None]
    filtered: list[FilteredEntry] = field(default_factory=list)
    errors: list[ErrorEntry] = field(default_factory=list)


@dataclass
class ExtractedRuleRecord:
    """One extracted rule serialized by a worker for parent aggregation.

    ``cgr_key`` is the atom-numbering-invariant canonical key from
    :func:`synplan.chem.utils.canonical_query_cgr_key`. The parent buckets
    rules by this key, so chemically-identical rules emitted from different
    workers collapse to one entry regardless of atom numbering.
    ``rule_smarts`` is the final SMARTS string written to the rules TSV. The
    parent intentionally does not parse this string back into a ReactionContainer:
    some query SMARTS generated by Chython can fail parser round-trips while
    still being valid output for ``Reactor.from_smarts`` downstream.
    """

    cgr_key: str
    rule_smarts: str
    reactor_validation: str | None = None


@dataclass
class ExtractionBatchResult:
    """Output of one rule-extraction worker batch.

    rule_records holds ``(reaction_index, rules, product_smi)`` tuples, where
    each rule is an :class:`ExtractedRuleRecord`. This is still lightweight
    compared with sending ReactionContainer pickles over IPC.

    ``audit_entries`` collects informational per-reaction skip reasons
    (e.g. ``SkippedMultiProduct``, ``NoRulesExtracted``) that are not real
    errors but explain why a reaction did not contribute to the final rule
    set. Parent aggregates these into a per-reaction audit file alongside
    the streaming error TSV.
    """

    rule_records: list[tuple[int, list[ExtractedRuleRecord], str]]
    errors: list[ErrorEntry]
    n_multi_product: int
    audit_entries: list[ErrorEntry] = field(default_factory=list)


@dataclass
class PipelineSummary:
    """Aggregate statistics from a pipeline run."""

    total_input: int = 0
    succeeded: int = 0
    filtered: int = 0
    errored: int = 0
    duplicates: int = 0
    elapsed_seconds: float = 0.0
    error_file: str | None = None
    error_breakdown: dict[str, int] = field(default_factory=dict)
    filter_breakdown: dict[str, int] = field(default_factory=dict)

    @property
    def total_output(self) -> int:
        return self.succeeded

    @property
    def total_excluded(self) -> int:
        return self.filtered + self.errored + self.duplicates

    def to_dict(self) -> dict:
        return asdict(self)

    def to_json(self, path: str | Path | None = None, **kwargs) -> str:
        """Serialize to JSON string. Optionally write to file."""
        data = self.to_dict()
        s = json.dumps(data, indent=2, default=str, **kwargs)
        if path is not None:
            Path(path).write_text(s)
        return s
